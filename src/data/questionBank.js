export function pickQuestions(areaId, type, count) {
  const pool = questionBank.filter(q => q.area === areaId && (q.type === type || q.type === 'both'))
  const shuffled = [...pool].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, Math.min(count, shuffled.length))
}

export function pickQuestionsByTopic(areaId, topic, type) {
  return questionBank
    .filter(q => q.area === areaId && q.topic === topic && (q.type === type || q.type === 'both'))
    .sort(() => Math.random() - 0.5)
}

export function shuffleOptions(question) {
  const opts = question.opts.map((text, i) => ({ text, origIdx: i }))
  const shuffled = [...opts].sort(() => Math.random() - 0.5)
  return {
    ...question,
    opts: shuffled.map(o => o.text),
    ans: shuffled.findIndex(o => o.origIdx === question.ans),
  }
}
